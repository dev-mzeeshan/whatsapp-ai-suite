# --------------------------------------------------------------------------- #
#  Helpers                                                                      #
# --------------------------------------------------------------------------- #

def utcnow() -> datetime:
    return datetime.now(timezone.utc)


def new_uuid() -> uuid.UUID:
    return uuid.uuid4()


# --------------------------------------------------------------------------- #
#  Enums (stored as VARCHAR in DB for readability in raw SQL queries)          #
# --------------------------------------------------------------------------- #

class SenderType(str, PyEnum):
    USER  = "USER"   # inbound message from WhatsApp user
    BOT   = "BOT"    # outbound message generated by LLM
    AGENT = "AGENT"  # outbound message typed manually by a human operator

class MessageStatus(str, PyEnum):
    SENT      = "SENT"
    DELIVERED = "DELIVERED"
    READ      = "READ"
    FAILED    = "FAILED"

class ConversationStatus(str, PyEnum):
    OPEN    = "OPEN"
    CLOSED  = "CLOSED"
    PENDING = "PENDING"  # waiting for agent review


# --------------------------------------------------------------------------- #
#  Contacts                                                                     #
# --------------------------------------------------------------------------- #

class Contact(Base):
    """
    One row per unique WhatsApp user (identified by wa_id = phone number).
    Created automatically on first inbound message.
    """
    __tablename__ = "contacts"

    id: Mapped[uuid.UUID] = mapped_column(
        UUID(as_uuid=True), primary_key=True, default=new_uuid
    )
    wa_id: Mapped[str] = mapped_column(
        String(20), nullable=False, unique=True, index=True,
        comment="WhatsApp ID — E.164 phone number without the + sign"
    )
    tenant_id: Mapped[uuid.UUID] = mapped_column(
        UUID(as_uuid=True),
        ForeignKey("tenants.id", ondelete="CASCADE"),
        nullable=False,
        index=True,
    )
    name: Mapped[str | None] = mapped_column(String(255), nullable=True)
    phone_number: Mapped[str | None] = mapped_column(String(20), nullable=True)
    is_blocked: Mapped[bool] = mapped_column(Boolean, default=False, nullable=False)

    created_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True), default=utcnow, nullable=False
    )
    updated_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True), default=utcnow, onupdate=utcnow, nullable=False
    )

    # Relationships
    conversations: Mapped[list["Conversation"]] = relationship(
        back_populates="contact", cascade="all, delete-orphan"
    )

    tenant: Mapped["Tenant"] = relationship(back_populates="contacts")

    def __repr__(self) -> str:
        return f"<Contact wa_id={self.wa_id} name={self.name}>"
    



# --------------------------------------------------------------------------- #
#  Conversations                                                                #
# --------------------------------------------------------------------------- #

class Conversation(Base):
    """
    Groups messages with a single contact into a conversation thread.
    One contact can have multiple conversations over time (e.g. re-opened).

    The is_bot_active flag is the core of Phase 5 human-in-the-loop control.
    When False, the webhook handler skips LLM processing for this conversation.
    """
    __tablename__ = "conversations"

    id: Mapped[uuid.UUID] = mapped_column(
        UUID(as_uuid=True), primary_key=True, default=new_uuid
    )
    contact_id: Mapped[uuid.UUID] = mapped_column(
        UUID(as_uuid=True), ForeignKey("contacts.id", ondelete="CASCADE"),
        nullable=False, index=True
    )

    status: Mapped[ConversationStatus] = mapped_column(
        Enum(ConversationStatus, name="conversation_status_enum"),
        default=ConversationStatus.OPEN,
        nullable=False,
    )

    # ------------------------------------------------------------------ #
    #  Bot control — SOURCE OF TRUTH for human-in-the-loop               #
    # ------------------------------------------------------------------ #
    is_bot_active: Mapped[bool] = mapped_column(
        Boolean, default=True, nullable=False,
        comment="When False, AI processing is skipped. Operator handles manually."
    )

    # Optional: which human agent is handling this if bot is paused
    assigned_agent_id: Mapped[uuid.UUID | None] = mapped_column(
        UUID(as_uuid=True), nullable=True
    )

    # System prompt override for this specific conversation
    system_prompt: Mapped[str | None] = mapped_column(Text, nullable=True)

    # Denormalised for fast sidebar sorting — updated on every new message
    last_message_at: Mapped[datetime | None] = mapped_column(
        DateTime(timezone=True), nullable=True, index=True
    )
    last_message_preview: Mapped[str | None] = mapped_column(
        String(255), nullable=True
    )
    unread_count: Mapped[int] = mapped_column(Integer, default=0, nullable=False)

    created_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True), default=utcnow, nullable=False
    )
    updated_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True), default=utcnow, onupdate=utcnow, nullable=False
    )

    # Relationships
    contact: Mapped["Contact"] = relationship(back_populates="conversations")
    messages: Mapped[list["Message"]] = relationship(
        back_populates="conversation",
        cascade="all, delete-orphan",
        order_by="Message.created_at",
    )

    __table_args__ = (
        Index("ix_conversations_contact_last_msg", "contact_id", "last_message_at"),
    )

    def __repr__(self) -> str:
        return f"<Conversation id={self.id} bot_active={self.is_bot_active}>"


