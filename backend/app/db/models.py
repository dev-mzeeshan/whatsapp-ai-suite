#  Messages                                                                     #
# --------------------------------------------------------------------------- #

class Message(Base):
    """
    Every single message — inbound and outbound — is stored here.
    This is the append-only log. Nothing is ever deleted.

    sender_type distinguishes whether the message came from:
      USER  = WhatsApp user (inbound)
      BOT   = AI-generated reply (outbound)
      AGENT = Human operator reply via dashboard (outbound)
    """
    __tablename__ = "messages"

    id: Mapped[uuid.UUID] = mapped_column(
        UUID(as_uuid=True), primary_key=True, default=new_uuid
    )
    conversation_id: Mapped[uuid.UUID] = mapped_column(
        UUID(as_uuid=True),
        ForeignKey("conversations.id", ondelete="CASCADE"),
        nullable=False,
        index=True,
    )

    # The wamid Meta assigns to every message (e.g. wamid.HBgLMTIzNDU2Nzg5...)
    # Nullable because BOT/AGENT messages get this assigned after Meta ACKs
    meta_message_id: Mapped[str | None] = mapped_column(
        String(255), nullable=True, unique=True, index=True
    )

    sender_type: Mapped[SenderType] = mapped_column(
        Enum(SenderType, name="sender_type_enum"),
        nullable=False,
        index=True,
    )
    content: Mapped[str] = mapped_column(Text, nullable=False)
    message_type: Mapped[str] = mapped_column(
        String(50), default="text", nullable=False,
        comment="text | image | audio | document | interactive"
    )

    status: Mapped[MessageStatus] = mapped_column(
        Enum(MessageStatus, name="message_status_enum"),
        default=MessageStatus.SENT,
        nullable=False,
    )

    # Timestamps
    created_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True), default=utcnow, nullable=False, index=True
    )
    delivered_at: Mapped[datetime | None] = mapped_column(
        DateTime(timezone=True), nullable=True
    )
    read_at: Mapped[datetime | None] = mapped_column(
        DateTime(timezone=True), nullable=True
    )

    # Relationship
    conversation: Mapped["Conversation"] = relationship(back_populates="messages")

    __table_args__ = (
        Index("ix_messages_conversation_created", "conversation_id", "created_at"),
    )

    def __repr__(self) -> str:
        return f"<Message id={self.id} type={self.sender_type} status={self.status}>"


