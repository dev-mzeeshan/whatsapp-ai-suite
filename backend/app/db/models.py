class BotConfig(Base):
    """
    Global bot configuration — one row for the whole system.
    Use get_or_create pattern; never insert more than one row.
    """
    __tablename__ = "bot_config"

    id: Mapped[int] = mapped_column(Integer, primary_key=True, default=1)
    default_system_prompt: Mapped[str] = mapped_column(
        Text,
        nullable=False,
        default=(
            "You are a helpful WhatsApp business assistant. "
            "Be concise, friendly, and professional. "
            "Keep replies under 3 sentences unless the user asks for detail."
        ),
    )
    max_context_messages: Mapped[int] = mapped_column(
        Integer, default=20, nullable=False
    )
    is_globally_active: Mapped[bool] = mapped_column(
        Boolean, default=True, nullable=False
    )

    updated_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True), default=utcnow, onupdate=utcnow, nullable=False
    )

    __table_args__ = (
        UniqueConstraint("id", name="uq_bot_config_singleton"),
    )

