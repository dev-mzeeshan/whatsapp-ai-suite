class TenantStatus(str, PyEnum):
    ACTIVE   = "ACTIVE"
    INACTIVE = "INACTIVE"
    SUSPENDED = "SUSPENDED"

class UserRole(str, PyEnum):
    SUPER_ADMIN = "SUPER_ADMIN"  # tum — full platform control
    TENANT_ADMIN = "TENANT_ADMIN"  # client — sirf apna data


class Tenant(Base):
    """
    Har client ek Tenant hai.
    Ek tenant = ek WhatsApp number = ek business.
    """
    __tablename__ = "tenants"

    id: Mapped[uuid.UUID] = mapped_column(
        UUID(as_uuid=True), primary_key=True, default=new_uuid
    )
    business_name: Mapped[str] = mapped_column(String(255), nullable=False)
    owner_email: Mapped[str] = mapped_column(
        String(255), nullable=False, unique=True, index=True
    )

    # Meta credentials — har client ke apne
    phone_number_id: Mapped[str] = mapped_column(
        String(50), nullable=False, unique=True, index=True,
        comment="Meta phone_number_id — webhook routing ke liye"
    )
    whatsapp_number: Mapped[str] = mapped_column(String(20), nullable=False)
    meta_access_token: Mapped[str] = mapped_column(Text, nullable=False)

    # n8n bot webhook — yahan forward karenege inbound messages
    n8n_webhook_url: Mapped[str | None] = mapped_column(Text, nullable=True)

    # Limits — tum set karo per client
    monthly_message_limit: Mapped[int] = mapped_column(
        Integer, default=1000, nullable=False
    )
    current_month_messages: Mapped[int] = mapped_column(
        Integer, default=0, nullable=False
    )

    status: Mapped[TenantStatus] = mapped_column(
        Enum(TenantStatus, name="tenant_status_enum"),
        default=TenantStatus.ACTIVE,
        nullable=False,
    )

    created_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True), default=utcnow, nullable=False
    )
    updated_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True), default=utcnow, onupdate=utcnow, nullable=False
    )

    # Relationships
    users: Mapped[list["TenantUser"]] = relationship(
        back_populates="tenant", cascade="all, delete-orphan"
    )
    contacts: Mapped[list["Contact"]] = relationship(
        back_populates="tenant", cascade="all, delete-orphan"
    )

    def __repr__(self) -> str:
        return f"<Tenant {self.business_name} | {self.whatsapp_number}>"


class TenantUser(Base):
    """
    Platform users — Super Admin ya Tenant Admin.
    Ek tenant ke multiple users ho sakte hain (e.g. multiple operators).
    """
    __tablename__ = "tenant_users"

    id: Mapped[uuid.UUID] = mapped_column(
        UUID(as_uuid=True), primary_key=True, default=new_uuid
    )
    tenant_id: Mapped[uuid.UUID | None] = mapped_column(
        UUID(as_uuid=True),
        ForeignKey("tenants.id", ondelete="CASCADE"),
        nullable=True,  # Super Admin ka koi tenant nahi hota
        index=True,
    )
    email: Mapped[str] = mapped_column(
        String(255), nullable=False, unique=True, index=True
    )
    hashed_password: Mapped[str] = mapped_column(String(255), nullable=False)
    full_name: Mapped[str] = mapped_column(String(255), nullable=False)
    role: Mapped[UserRole] = mapped_column(
        Enum(UserRole, name="user_role_enum"),
        nullable=False,
        default=UserRole.TENANT_ADMIN,
    )
    is_active: Mapped[bool] = mapped_column(Boolean, default=True, nullable=False)

    created_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True), default=utcnow, nullable=False
    )

    # Relationships
    tenant: Mapped["Tenant | None"] = relationship(back_populates="users")

    def __repr__(self) -> str:
        return f"<TenantUser {self.email} | {self.role}>"


